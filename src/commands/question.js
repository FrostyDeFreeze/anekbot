const ranges = {
	emoticons: [0x1f600, 0x1f64f],
	food: [0x1f32d, 0x1f37f],
	animals: [0x1f400, 0x1f4d3],
	expressions: [0x1f910, 0x1f92f]
}

const randEmoji = function (range) {
	const [min, max] = range
	const codePoint = Math.floor(Math.random() * (max - min + 1)) + min
	return String.fromCodePoint(codePoint)
}

const randEmojis = count => {
	const categories = Object.values(ranges)
	const result = []

	for (let i = 0; i < count; i++) {
		const randCategory = categories[Math.floor(Math.random() * categories.length)]
		result.push(randEmoji(randCategory))
	}

	return result.join(` `)
}

